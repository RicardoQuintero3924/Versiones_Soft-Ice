package Beans;

public class BeansNotaCredito {

    private String numeroNC;
    private String referencianc;
    private String cantidadnc;
    private String autorizadorpor;
    private String abierta_cerrada;

    public String getNumeroNC() {
        return numeroNC;
    }

    public void setNumeroNC(String numeroNC) {
        this.numeroNC = numeroNC;
    }

    public String getReferencianc() {
        return referencianc;
    }

    public void setReferencianc(String referencianc) {
        this.referencianc = referencianc;
    }

    public String getCantidadnc() {
        return cantidadnc;
    }

    public void setCantidadnc(String cantidadnc) {
        this.cantidadnc = cantidadnc;
    }

    public String getAutorizadorpor() {
        return autorizadorpor;
    }

    public void setAutorizadorpor(String autorizadorpor) {
        this.autorizadorpor = autorizadorpor;
    }

    public String getAbierta_cerrada() {
        return abierta_cerrada;
    }

    public void setAbierta_cerrada(String abierta_cerrada) {
        this.abierta_cerrada = abierta_cerrada;
    }

}
