package Beans;

public class BeansSobrantesFaltantes {

    private String referenciaSF;
    private String sobrantes;
    private String faltantes;
    private String observaciones;
    private String motivo;

    public String getReferenciaSF() {
        return referenciaSF;
    }

    public void setReferenciaSF(String referenciaSF) {
        this.referenciaSF = referenciaSF;
    }

    public String getSobrantes() {
        return sobrantes;
    }

    public void setSobrantes(String sobrantes) {
        this.sobrantes = sobrantes;
    }

    public String getFaltantes() {
        return faltantes;
    }

    public void setFaltantes(String faltantes) {
        this.faltantes = faltantes;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

}
